import Foundation
import UIKit

extension UIColor {

    class func flk_appThemeColor() -> UIColor {
        return UIColor(red: 244/255, green: 179/255, blue: 80/255, alpha: 1)
    }
    
    class func flk_cellThemeColor() -> UIColor {
        return UIColor(red: 240/255, green: 179/255, blue: 80/255, alpha: 1)
    }
}